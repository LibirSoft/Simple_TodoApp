package com.libirsoft.herba;

public class To_DO {
    String DATE;
    String TODO;
    int ISDONE;

    public String getDATE() {
        return DATE;
    }

    public void setDATE(String DATE) {
        this.DATE = DATE;
    }

    public String getTODO() {
        return TODO;
    }

    public void setTODO(String TODO) {
        this.TODO = TODO;
    }

    public int getISDONE() {
        return ISDONE;
    }

    public void setISDONE(int ISDONE) {
        this.ISDONE = ISDONE;
    }
}
